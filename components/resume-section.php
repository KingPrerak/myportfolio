<script>
// Enhanced Skills Section JavaScript
document.addEventListener('DOMContentLoaded', function() {
    const skillsSection = document.getElementById('skills-section');
    const skillCards = document.querySelectorAll('.skill-card-modern');
    const filterBtns = document.querySelectorAll('.filter-btn');
    const skillsGrid = document.getElementById('skills-grid');

    // Intersection Observer for section animation
    const sectionObserver = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.remove('skills-section-hidden');
                entry.target.classList.add('skills-section-visible');

                // Stagger card animations
                skillCards.forEach((card, index) => {
                    setTimeout(() => {
                        card.style.animation = 'skillFadeInUp 0.6s ease-out forwards';
                        card.style.animationDelay = `${index * 0.1}s`;
                    }, index * 100);
                });
            }
        });
    }, {
        threshold: 0.2,
        rootMargin: '0px 0px -100px 0px'
    });

    if (skillsSection) {
        sectionObserver.observe(skillsSection);
    }

    // Category filtering functionality
    filterBtns.forEach(btn => {
        btn.addEventListener('click', function() {
            const filter = this.dataset.filter;
            
            // Update active button
            filterBtns.forEach(b => b.classList.remove('active'));
            this.classList.add('active');
            
            // Filter skills
            skillCards.forEach(card => {
                const category = card.dataset.category;
                
                if (filter === 'all' || category === filter) {
                    card.style.display = 'block';
                    card.style.animation = 'skillFadeInUp 0.4s ease-out forwards';
                } else {
                    card.style.animation = 'skillFadeOut 0.3s ease-out forwards';
                    setTimeout(() => {
                        card.style.display = 'none';
                    }, 300);
                }
            });
        });
    });

    // Individual card interactions
    skillCards.forEach(card => {
        // Magnetic hover effect
        card.addEventListener('mousemove', function(e) {
            const rect = this.getBoundingClientRect();
            const x = e.clientX - rect.left;
            const y = e.clientY - rect.top;
            
            const centerX = rect.width / 2;
            const centerY = rect.height / 2;
            
            const rotateX = (y - centerY) / 10;
            const rotateY = (centerX - x) / 10;
            
            this.style.transform = `perspective(1000px) rotateX(${rotateX}deg) rotateY(${rotateY}deg) scale3d(1.05, 1.05, 1.05)`;
        });

        card.addEventListener('mouseleave', function() {
            this.style.transform = 'perspective(1000px) rotateX(0deg) rotateY(0deg) scale3d(1, 1, 1)';
        });

        // Progress bar animation on hover
        card.addEventListener('mouseenter', function() {
            const progress = this.querySelector('.skill-progress');
            const value = progress.dataset.progress;
            progress.style.background = `conic-gradient(#00bcd4 0deg, #2196f3 ${value * 3.6}deg, rgba(255,255,255,0.1) ${value * 3.6}deg)`;
        });
    });

    // Add floating animation delays
    skillCards.forEach((card, index) => {
        const randomDelay = Math.random() * 2;
        card.style.setProperty('--float-delay', `${randomDelay}s`);
    });

    // Parallax effect on scroll
    window.addEventListener('scroll', function() {
        const scrolled = window.pageYOffset;
        const parallax = document.querySelector('.skills-particles');
        if (parallax) {
            const speed = scrolled * 0.5;
            parallax.style.transform = `translateY(${speed}px)`;
        }
    });
});

// Add new animation keyframes
const style = document.createElement('style');
style.textContent = `
    @keyframes skillFadeOut {
        from {
            opacity: 1;
            transform: scale(1);
        }
        to {
            opacity: 0;
            transform: scale(0.8);
        }
    }
`;
document.head.appendChild(style);
</script>
